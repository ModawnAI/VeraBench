// Provider logo wrapper component
interface ProviderLogoProps {
  providerId: string;
  className?: string;
  size?: number;
}

export function ProviderLogo({ providerId, className, size = 24 }: ProviderLogoProps) {
  switch (providerId) {
    case "openai":
      return <OpenAILogo className={className} size={size} />;
    case "anthropic":
      return <AnthropicLogo className={className} size={size} />;
    case "google":
      return <GeminiLogo className={className} size={size} />;
    case "deepseek":
      return <DeepSeekLogo className={className} size={size} />;
    case "xai":
      return <XAILogo className={className} size={size} />;
    default:
      return (
        <div
          className={cn("bg-[#DDD] rounded", className)}
          style={{ width: size, height: size }}
        />
      );
  }
}
